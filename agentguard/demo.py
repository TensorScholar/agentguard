from __future__ import annotations

from dataclasses import dataclass
from pathlib import Path

from .policy_packs import render_policy_pack


@dataclass(frozen=True)
class DemoFile:
    relative_path: str
    content: str


def write_demo(output_dir: Path, force: bool = False) -> list[Path]:
    if output_dir.exists() and any(output_dir.iterdir()) and not force:
        raise FileExistsError(f"refusing to overwrite non-empty demo directory: {output_dir}")

    output_dir.mkdir(parents=True, exist_ok=True)
    written: list[Path] = []
    for item in demo_files(output_dir):
        path = output_dir / item.relative_path
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(item.content, encoding="utf-8")
        written.append(path)
    return written


def demo_files(output_dir: Path | None = None) -> tuple[DemoFile, ...]:
    demo_dir = str(output_dir or Path(".agentguard/demo"))
    return (
        DemoFile("policy.yaml", render_policy_pack("coding-agent-local")),
        DemoFile("safe_mcp_config.json", SAFE_MCP_CONFIG),
        DemoFile("dangerous_mcp_config.json", DANGEROUS_MCP_CONFIG),
        DemoFile("tool_calls.jsonl", TOOL_CALLS_JSONL),
        DemoFile("README.md", render_demo_readme(demo_dir)),
    )


