def render_demo_readme(demo_dir: str) -> str:
    return f"""# AgentGuard Local Demo

This directory is generated by `agentguard demo`.

Run the safe scan:

```bash
python -m agentguard.cli scan --config {demo_dir}/safe_mcp_config.json --format markdown
```

Run the dangerous scan:

```bash
python -m agentguard.cli scan --config {demo_dir}/dangerous_mcp_config.json --format markdown
```

Run the CI-style gate. This should fail because the dangerous config exposes credentials,
production mutation, shell execution, and cloud tooling:

```bash
python -m agentguard.cli gate --config {demo_dir}/dangerous_mcp_config.json --fail-on-risk high
```

Run the local policy proxy demo:

```bash
python -m agentguard.cli proxy \
  --policy {demo_dir}/policy.yaml \
  --ledger {demo_dir}/audit.sqlite \
  < {demo_dir}/tool_calls.jsonl
```

Render the audit report:

```bash
python -m agentguard.cli report --ledger {demo_dir}/audit.sqlite --format markdown
```
"""
